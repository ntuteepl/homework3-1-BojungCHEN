#include <stdio.h>

int main() {
    int s1, d1, s2, d2, s3, d3;

    // 输入三笔订单的发车时间和返回时间
    scanf("%d %d %d %d %d %d", &s1, &d1, &s2, &d2, &s3, &d3);

    // 初始化巴士数量为1，因为第一笔订单至少需要一辆巴士
    int busCount = 1;

    // 逐个检查后续订单
    // 如果下一个订单的发车时间早于或等于前一个订单的返回时间，则使用同一辆巴士
    if (s2 <= d1) {
        d1 = d2; // 更新前一个订单的返回时间
    } else if (s3 <= d1) {
        d1 = d3; // 更新前一个订单的返回时间
    } else {
        // 如果下一个订单无法使用同一辆巴士，则需要为下一个订单分配一辆新的巴士
        busCount++;
        d1 = d2; // 使用第二笔订单的返回时间作为新的返回时间
    }

    // 输出至少需要的巴士数量
    printf("%d \n", busCount);

    return 0;
}

